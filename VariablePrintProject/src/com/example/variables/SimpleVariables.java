package com.example.variables;

public class SimpleVariables {
    public static void main(String[] args) {

        byte age;
        age = 25;


        short s;
        s = 32000;

        int averageSalary;
        averageSalary = 50000;

        long annualSalary;
        annualSalary = 200000L;

        float price;
        price = 19.99f;

        double annualSale;
        annualSale = 500000.99;

        char c;
        c = 'A';

        boolean bl;
        bl = true;

        System.out.println("Byte: " + age);
        System.out.println("Short: " + s);
        System.out.println("Int: " + averageSalary);
        System.out.println("Long: " + annualSalary);
        System.out.println("Float: " + price);
        System.out.println("Double: " + annualSale);
        System.out.println("Char: " + c);
        System.out.println("Boolean: " + bl);

    }
}
