// Task: Write the code to convert decimal numbers to binary format?
package com.PracticeSets.DecimalToBinary;

public class DecimalToBinary {
    public static void main(String[] args) {
        int num;
        num = 10;

        // Convert the decimal number to binary using Integer.toBinaryString() method
        // Print the binary value equivalent of the decimal number
        System.out.println(Integer.toBinaryString(num));
    }

}
